interface TranslationContextType {
  language: Language;
  toggleLanguage: (lang: Language) => void;
  t: (key: keyof typeof TRANSLATIONS.en) => string;
  isRtl: boolean;
}

const TranslationContext = createContext<TranslationContextType | undefined>(undefined);

export function TranslationProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>("en");

  useEffect(() => {
    if (typeof window !== "undefined") {
      const stored = localStorage.getItem("retailiq_lang") as Language;
      if (stored === "en" || stored === "ar") {
        setLanguage(stored);
        applyLanguage(stored);
      }
    }
  }, []);

  const applyLanguage = (lang: Language) => {
    const isRtl = lang === "ar";
    document.documentElement.dir = isRtl ? "rtl" : "ltr";
    document.documentElement.lang = lang;
    if (isRtl) {
      document.body.classList.add("rtl");
    } else {
      document.body.classList.remove("rtl");
    }
  };

  const toggleLanguage = (lang: Language) => {
    setLanguage(lang);
    applyLanguage(lang);
    localStorage.setItem("retailiq_lang", lang);
  };

  const t = (key: keyof typeof TRANSLATIONS.en): string => {
    const dict = TRANSLATIONS[language] || TRANSLATIONS.en;
    return (dict as any)[key] || (TRANSLATIONS.en as any)[key] || key;
  };

  const isRtl = language === "ar";

  return (
    <TranslationContext.Provider value={{ language, toggleLanguage, t, isRtl }}>
      {children}
    </TranslationContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(TranslationContext);
  if (!context) {
    throw new Error("useI18n must be used within a TranslationProvider");
  }
  return context;
}
